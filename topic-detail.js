const topicRoot = document.querySelector("#topicDetail");
const topicId = new URLSearchParams(window.location.search).get("id");
const topic = topics[topicId];

function getSuggestedBookLink(title) {
  const bookIndex = window.libraryBooks?.findIndex((book) => book.title === title);
  return bookIndex >= 0 ? `book.html?id=${bookIndex}` : null;
}

function renderSuggestion(title) {
  const href = getSuggestedBookLink(title);
  return href
    ? `<li><a href="${href}">${title}</a></li>`
    : `<li>${title}</li>`;
}

function renderMissingTopic() {
  topicRoot.innerHTML = `
    <div class="topic-copy">
      <p class="eyebrow">Không tìm thấy nội dung</p>
      <h1>Nội dung này chưa được cập nhật.</h1>
      <p>Vui lòng quay lại trang chính để chọn một mục khác.</p>
      <a class="button primary" href="index.html#home">Quay lại trang chính</a>
    </div>
  `;
}

function renderTopic(topicData) {
  document.title = `${topicData.title} - Thư viện tỉnh Tây Ninh`;
  topicRoot.innerHTML = `
    <div class="topic-copy">
      <p class="eyebrow">${topicData.label}</p>
      <h1>${topicData.title}</h1>
      <p class="topic-intro">${topicData.intro}</p>
      <div class="topic-sections">
        ${topicData.details.map((item, index) => `
          <article>
            <span>${String(index + 1).padStart(2, "0")}</span>
            <p>${item}</p>
          </article>
        `).join("")}
      </div>
      <section class="topic-suggestions">
        <h2>Sách gợi ý</h2>
        <ul>
          ${topicData.suggestions.map(renderSuggestion).join("")}
        </ul>
      </section>
      <div class="detail-actions">
        <a class="button primary" href="index.html#sach">Xem danh mục sách</a>
        <a class="button outline" href="index.html#lien-he">Liên hệ thư viện</a>
      </div>
    </div>
  `;
}

async function initTopicDetail() {
  if (!topicRoot) {
    return;
  }

  await window.libraryBooksReady;

  if (!topic) {
    renderMissingTopic();
    return;
  }

  renderTopic(topic);
}

initTopicDetail();
